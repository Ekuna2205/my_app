class QuizScreen extends StatefulWidget {
  final String categoryName;
  final Color color;
  final IconData icon;
  final List<Question> questions;
  final Function(int) onComplete;

  const QuizScreen({
    super.key,
    required this.categoryName,
    required this.color,
    required this.icon,
    required this.questions,
    required this.onComplete,
  });

  @override
  State<QuizScreen> createState() => _QuizScreenState();
}

class _QuizScreenState extends State<QuizScreen> {
  late List<Question> shuffledQuestions;
  late List<List<String>> shuffledOptions;
  late List<int> correctIndices;
  int index = 0;
  int score = 0;
  int timeLeft = 30;
  late Timer timer;
  late ConfettiController confetti;

  @override
  void initState() {
    super.initState();
    final random = Random();
    shuffledQuestions = List.from(widget.questions)..shuffle(random);

    shuffledOptions = [];
    correctIndices = [];
    for (var q in shuffledQuestions) {
      final opts = List<String>.from(q.options);
      final correct = q.correctIndex;
      final indices = List<int>.generate(opts.length, (i) => i)
        ..shuffle(random);
      final newOpts = indices.map((i) => opts[i]).toList();
      final newCorrect = indices.indexOf(correct);
      shuffledOptions.add(newOpts);
      correctIndices.add(newCorrect);
    }

    confetti = ConfettiController(duration: const Duration(seconds: 3));
    _startTimer();
  }

  void _startTimer() {
    timer = Timer.periodic(const Duration(seconds: 1), (_) {
      setState(() {
        if (timeLeft > 0) {
          timeLeft--;
        } else {
          score -= 5;
          _next();
        }
      });
    });
  }

  void _next() {
    timer.cancel();
    if (index < shuffledQuestions.length - 1) {
      setState(() {
        index++;
        timeLeft = 30;
      });
      _startTimer();
    } else {
      widget.onComplete(score);
      if (score > shuffledQuestions.length * 5) confetti.play();

      showDialog(
        context: context,
        barrierDismissible: false,
        builder: (_) => AlertDialog(
          shape:
              RoundedRectangleBorder(borderRadius: BorderRadius.circular(20)),
          backgroundColor: Theme.of(context).cardColor,
          title: const Text('🎉 Дууслаа!'),
          content: Text('Таны оноо: $score / ${shuffledQuestions.length * 10}',
              style: const TextStyle(fontSize: 22)),
          actions: [
            ElevatedButton(
              style: ElevatedButton.styleFrom(backgroundColor: widget.color),
              onPressed: () =>
                  Navigator.popUntil(context, (route) => route.isFirst),
              child: const Text('Нүүр хуудас руу',
                  style: TextStyle(color: Colors.white)),
            ),
          ],
        ),
      );
    }
  }

  void answer(int selected) {
    timer.cancel();
    if (selected == correctIndices[index]) {
      score += 10;
    } else {
      score -= 5;
    }
    _next();
  }

